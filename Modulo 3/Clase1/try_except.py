#Estructura del try except
# El try contiene el codigo que puede tener una excepción o un error
try:
    print(0)
# Maneja la excepcion especifica o generica que ocurre en el bloque try
except print(0):
    pass
# El else es opcional y las instrucciones que contiene se ejecutan solo si no ocurre una excepción
else:
    pass
# El finally es opcional y las instrucciones que contiene se ejecutan si ocurre o no una excepción
finally:
    pass