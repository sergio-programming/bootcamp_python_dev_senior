"""
Para declarar variables escribimos el nombre de la variable, despues usamos el operador de asignacion = y por
escribimos el contenido que va a almacenar esta variable
"""
miPrimeraVariable = "Hola programadores"
print(miPrimeraVariable)

#Tipos de datos de las variables
"""
Tipo de dato String: Almacenan cadenas de texto y se colocan entre comillas simples o dobles
"""
nombre = "Sergio Pedraza"
print(f"Hola, mi nombre es {nombre}")
print(f"La variable nombre es de tipo: {type(nombre)}")

"""
Tipo de dato entero: Almacenan numeros sin parte decimal
"""
a = 12
print(f"La variable a contiene el numero: {a}")
print(f"La variable a es de tipo: {type(a)}")

"""
Tipo de dato float: Almacenan numeros con parte decimal
"""
b = 10.5
print(f"La variable b contiene el numero: {b}")
print(f"La variable b es de tipo: {type(b)}")

"""
Tipo de dato boolean: Almacena unicamente dos valores: True o False
"""
alerta = True
print(f"La variable alerta contiene el valor: {alerta}")
print(f"La variable alerta es de tipo: {type(alerta)}")